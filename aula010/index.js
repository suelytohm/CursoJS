/**
 * Operadores aritméticos
 * + Adição / Concatenação (- * /)
 * ** Potenciação
 * % Resto de divisão
 */

let contador = 1;
/**
 * Atribui o valor após a execução
 * contador++
 *  */ 
console.log(contador++); // 1

/**
 * Atribui o valor antes da execução
 * ++contador
 *  */ 

console.log(++contador); // 3

/**
 * Diminui o valor após a execução
 * contador--
 *  */ 
console.log(contador--); // 3

/**
 * Diminui o valor antes da execução
 * --contador
 *  */ 
console.log(--contador); // 1

const num1 = 5;
const num2 = 2;
const num3 = 10;

console.log((num1 + num2) * num3); // 70
